/* Variables */

// Create a val for an immutable variable and a var for a mutable variable.
var mutable_variable = 5
val immutable_variable = 10

// Define an immutable variable x with the value of 10
val x = 10 // output: val x: Int = 10

// Define a mutable variable y with the value of 20 and change it to 25
var y = 20 // output: val y: Int = 20
var y = 25 // output: val y: Int = 25

/* Data Types */

// define variable of the following data type: Int, Double, Float, String
var a = 34 // output: val a: Int = 34
var b = 34.6f // output: val b: Float = 34.6
var c = 34.6 // output: val c: Double = 34.6
var d = "Crowdstrike falcon runs on ring 0" // output: val d: String = Crowdstrike falcon runs on ring 0
var e = true // output: val e: Boolean = true

// type conversion
var f = "1" // output: val f: String = 1
var h = f.toInt // output: val h: Int = 1

